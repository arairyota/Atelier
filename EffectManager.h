#ifndef EffectManager_H
#define EffectManager_H

//////////////////////////////////////////////////////////////////////////////
//ヘッダーファイルインクルード
//////////////////////////////////////////////////////////////////////////////
#include <vector>

//////////////////////////////////////////////////////////////////////////////
//define定義
//////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
//前方宣言
//////////////////////////////////////////////////////////////////////////////
class GameObject;

//////////////////////////////////////////////////////////////////////////////
//EffectManagerクラス
//////////////////////////////////////////////////////////////////////////////
class EffectManager {
private:
	static EffectManager* _instance;
	std::vector<GameObject*> _effectList;

public:
	static void Create() {
		if (_instance == nullptr) {
			_instance = new EffectManager;
		}
	}

	static void Delete() {
		if (_instance != nullptr) {
			delete _instance;
			_instance = nullptr;
		}
	}

	static auto GetInstance() {
		if (_instance != nullptr) {
			return _instance;
		}
	}
};

#endif // !EffectManager_H

